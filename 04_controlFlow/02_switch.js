let num=2;
// break is used because , in switch case if the condition is true then it will execute all the cases after that case
// except the default case. So to stop that we use break.
switch(num){
    case 1:
        console.log("Number is 1");
        break;
    case 2:
        console.log("Number is 2");
        break;
    case 5:
        console.log("Number is 5");
        break;
    default:
        console.log("default case");
}
